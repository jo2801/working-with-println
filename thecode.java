public class Aufgabe4 {
	public static void main(String[] args){
		int a = 20;
		int b = 3;
		int plus = a+b;
		int minus = a-b;
		int mal = a*b;
		int durch = a/b;

		System.out.println("Addition:"+a+"+"+b+"="+plus);
		System.out.println("Subtraktion:"+a+"-"+b+"="+minus);
		System.out.println("Multiplikation:"+a+"x"+b+"="+mal);
		System.out.println("Division:"+a+":"+b+"="+durch);
	}
}